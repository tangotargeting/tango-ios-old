- (BOOL)application:(UIApplication *)application didFinishLaunchingWithOptions:(NSDictionary *)launchOptions {
    
    // required by Tango to show local notifications
    UIUserNotificationType types = UIUserNotificationTypeBadge | UIUserNotificationTypeSound | UIUserNotificationTypeAlert;
    UIUserNotificationSettings *mySettings = [UIUserNotificationSettings settingsForTypes:types categories:nil];
    [[UIApplication sharedApplication] registerUserNotificationSettings:mySettings];
    
    NSArray *tags = [[NSUserDefaults standardUserDefaults] valueForKey:@"TangoTags"];
    if (tags == nil) {
        tags = @[@"YourCampaignTags"];
        [[NSUserDefaults standardUserDefaults] setObject:tags forKey:@"TangoTags"];
    }
    [Tango initializeWithAPIKey:@"Q4Yrz8eHStKBEAX4Ds76B3F8xurzaV4U" tags:tags];
    
    // Handle launching from a notification
    [Tango application:application didFinishLaunchingWithOptions:launchOptions];
    
    // Override point for customization after application launch.
    return YES;
}

